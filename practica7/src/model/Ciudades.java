package model;

public class Ciudades {
    private String nombre;
    private int poblacion;
    private String clima;
    private int riesgo;;

    public Ciudades(String nombre, int poblacion, String clima, int riesgo){
        this.nombre=nombre;
        this.poblacion=poblacion;
        this.clima=clima;
        this.riesgo=riesgo;
    }

}
